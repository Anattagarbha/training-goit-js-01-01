/* используются для сравнения двух значений. Результатом своего 
выполнения возвращают буль - true или false, то есть «да» или «нет» */

"use strict";

let x = 5;
let y = 10;
let z = 5;

console.log("x > y", x > y); // false
console.log("x < y", x < y); // true
console.log("x < z", x < z); // false
console.log("x <= z", x <= z); // true
console.log("x === y", x === y); // false
console.log("x === z", x === z); // true
console.log("x !== y", x !== y); // true
console.log("x !== z", x !== z); // false
